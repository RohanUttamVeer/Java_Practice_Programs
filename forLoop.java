class forLoop{
	public static void main(String[] args){ /*String[] args or String args[] both are similar but most recommended is String[] args
	                                         *it creates string array called args
														  */
		for( int i = 1; i <= 10; ++i){ //syntax : for(initialExpression; testExpression; updateExpression){}
			System.out.println(i); //prints integers from 1 to 10
		}
	}
}